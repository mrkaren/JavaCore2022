package classwork.lesson11.calculator;

//պետք է ունենալ 4 մեթոդներ
// plus
// minus
// multiply
// divide
// մեթոդները պետք է ընդունեն երկու double տիպի թիվ
// անեն համարժեք գործողությունը, և արդյունքը վերադարձնեն
// Սարքել CalculatorDemo կլասսը, և տեստ ենել մեր մեթոդները
public class Calculator {

    double plus(double a, double b) {
        return a + b;
    }

    double minus(double a, double b) {
        return a - b;
    }

    double multiply(double a, double b) {
        return a * b;
    }

    double divide(double a, double b) {
        return a / b;
    }


}
